#include <stdio.h>
int main()
{
    int i, j;
    scanf("%o#%x", &i, &j); // 15#22
    // 8진수 15를 10진수로 변환하기
    // 1. 자릿수를 오른쪽부터 번호 매기기
    // 가장 오른쪽 숫자부터 시작하여 0부터 번호를 매깁니다.
    // 예를 들어, 8진수 15의 경우:
    // 자릿수 번호: 1   0
    // 8진수  숫자: 1   5
    // 2. 각 자릿수에 해당하는 8의 거듭제곱을 곱함
    // 각 자릿수 숫자에 대해 숫자 × 8^자릿수 번호를 계산합니다.
    // 1 * 8^1 = 1 * 8 = 8
    // 5 * 8^0 = 5 * 1 = 5
    // 3. 모두 더해서 10진수 완성
    // 8 + 5 = 13, 즉 8진수 15는 10진수 13

    // 16진수 22를 10진수로 변환하는 방법
    // 1. 16진수 자리값 이해하기
    // 16진수는 숫자와 알파벳을 이렇게 씁니다:
    /*
    | 16진수| 10진수 |
    | ---- | ---- |
    | 0\~9 | 0\~9 |
    | A    | 10   |
    | B    | 11   |
    | C    | 12   |
    | D    | 13   |
    | E    | 14   |
    | F    | 15   |
    */
    // 2. 계산 방법
    // ▶ 자릿수 나누기 (오른쪽부터 번호)
    // 16 진수: 2       2
    // 자릿수 : 16^1    16^0
    // ▶ 숫자 변환
    // 2 * 16^1 = 2 * 16 = 32
    // 2 * 16^0 = 2 * 1 = 2
    // ▶ 더하기
    // 32 + 2 = 34, 즉 16진수 22는 10진수 34
    printf("%d %d", i, j); // 13 34
}
#include <stdio.h>
int main()
{
    int j = 024, k = 24, L = 0x24, hap;
    // j: 8진수, k: 10진수, L: 16진수
    // 8진수 j를 10진수로 변환:
    // 2 * 8^1 + 4 * 8^0
    // 2 * 8 + 4 * 1
    // 16 + 4 = 20
    // 즉, j는 10진수로 20.
    // 16진수 L을 10진수로 변환:
    // 2 * 16^1 + 4 * 16^0
    // 2 * 16 + 4 * 1
    // 32 + 4
    // 36, 즉, L은 10진수로 36.
    hap = j + k + L; // 20 + 24 + 36 = 44 + 36 = 80
    printf("%d, %d, %d, %d\n", j, k, L, hap);
    // 20, 24, 36, 80
    return 0;
}

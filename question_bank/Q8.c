#include <stdio.h>
int main()
{
    int arr[3][3] = {1, 2, 3, 4, 5, 6, 7, 8, 9};
    /*
            | arr[0][0] | arr[0][1]  | arr[0][2]
    -----------------------------------------------
    arr[0]  |     1     |     2      |      3
    -----------------------------------------------
    arr[1]  |     4     |     5      |      6
    -----------------------------------------------
    arr[2]  |      7    |     8      |      9
    -----------------------------------------------
            | arr[2][0] |  arr[2][1]   | arr[2][2]
    */
    int *parr[2] = {arr[1], arr[2]};
    /*
    2개의 요소를 갖는 정수형 포인터 배열 parr을 선언하고,
    parr[0]에 배열 arr의 두 번째 행의 시작 주소를,
    parr[1]에 배열 arr의 세 번째 행의 시작 주소를 저장한다.

    parr[0][0]      parr[0][1]      parr[0][2]
    parr[0]+0   |   parr[0]+1   |   parr[0]+2
    -------------------------------------------
        4       |       5       |       6
    -------------------------------------------
        7       |       8       |       9
    -------------------------------------------
    parr[1]+0   |    parr[1]+1  |   parr[1]+2
    parr[1][0]       parr[1][1] |   parr[1][2]
    */
    printf("%d", parr[1][1] + *(parr[1] + 2) + **parr); // 21
    /*
    parr[1][1]=8
    *(parr[1]+2)=9
    *parr은 parr[0]을 의미하며,
    **parr은 parr[0][0], 즉 arr[1][0]
    **parr=4
    8+9+4=21
    */
    return 0;
}